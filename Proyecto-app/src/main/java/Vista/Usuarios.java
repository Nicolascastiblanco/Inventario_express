    public void listar() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1_usuarios.getModel();
        modelo.setRowCount(0);

        List<Usuario> lista = dao.listarUsuarios();
        for (Usuario u : lista) {
            modelo.addRow(new Object[]{
                u.getid_usuario(),
                u.getnombre_usuario(),
                u.getApellido(),
                u.getContrasena(),
                u.getRol()
            });
        }
    }

