    private void jButton1_eliminar_UsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_eliminar_UsuarioActionPerformed
        int fila = jTable1_usuarios.getSelectedRow();

        if (fila == 1) {
            JOptionPane.showMessageDialog(null, "Seleccione un usuario");
            return;
        }

        int id = Integer.parseInt(jTable1_usuarios.getValueAt(fila, 0).toString());

        if (dao.eliminar(id)) {
            JOptionPane.showMessageDialog(null, "Usuario eliminado");
            listar();
        } else {
            JOptionPane.showMessageDialog(null, "Error al eliminar");
        }
    }//GEN-LAST:event_jButton1_eliminar_UsuarioActionPerformed

